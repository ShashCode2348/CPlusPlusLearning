#include <iostream>
# include <limits>
using namespace std;

int main() {
	cout << "In bytes: " << endl;
	cout << "Size of type signed char is " << sizeof(signed char) << endl;
	cout << "Size of type unsigned char is " << sizeof(unsigned char) << endl;
	cout << "Size of type char is " << sizeof(char) << endl;
	cout << "Size of type short int is " << sizeof(short int) << endl;
	cout << "Size of type unsigned short int is " << sizeof(unsigned short int) << endl;
	cout << "Size of type int is " << sizeof(int) << endl;
	cout << "Size of type unsigned int is " << sizeof(unsigned int) << endl;
	cout << "Size of type long int is " << sizeof(long int) << endl;
	cout << "Size of type unsigned long int is " << sizeof(unsigned long int) << endl;
	cout << "Size of type long long int is " << sizeof(long long int) << endl;
	cout << "Size of type unsigned long long int is " << sizeof(unsigned long long int) << endl;
}
